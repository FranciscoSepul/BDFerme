----- LISTAR TODOS LOS PROVEEDORES -------
CREATE OR REPLACE PROCEDURE LISTARTODOSPROVEEDORES(C_MEMORIA OUT SYS_REFCURSOR)
AS
BEGIN 
  OPEN C_MEMORIA FOR SELECT PRO.ID, EM.RUTEMPRESA, EM.DVRUT, EM.NOMBTEEMPRESA, EM.CORREO, EM.NUMEROTEL,
                            PRO.RUNPROVEEDOR, PRO.DIGITOVERIF, PRO.ESTADO, PRO.NOMBRE, PRO.NUMEROTEL, 
                            PRO.DIRECCION, PRO.CORREO
                            FROM PROVEEDOR PRO INNER JOIN EMPRESA EM ON PRO.IDEMPRESA = EM.ID;                       
END;


set autoprint on;
